
public class IntLiteral {

	public static void main(String[] args) {
		System.out.println("Int Literal");
		/*
		 * literal are value directly encoded which does not need any evaluation
		 * 0 is for octal
		 * 0x is for hexadecimal
		 * 0b is for binary
		 * + will be present in output
		 * */
		System.out.println(+1066);
		System.out.println(-1066);
		System.out.println(011);
		System.out.println(0x11);
		System.out.println(0xAB);
		System.out.println(0x11AB);
		System.out.println(0b101);
		
		
		
	}

}
