
public class FloatingLiteral {
	public static void main(String[] args) {
		/*
		 * Floating point Literals
		 * F,f denote float of 4 bytes
		 * D,d or specified nothing is denoted as double
		 * E is exponent to the power 10 eg: 1.0E3 is 1*10^3 =1000.0 
		 * E-3 denote 1/10^3*/
		System.out.println(10.66f);
		System.out.println(13.67F);
		System.out.println(12.89);
		System.out.println(12.78D);
		System.out.println(156.90d);
		System.out.println(.6);
		System.out.println(145.0);
		System.out.println(1.0E3);
		System.out.println(1.0E-3);
	}

}
